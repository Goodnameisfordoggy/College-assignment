#include <iostream>
using namespace std;

const int MAXSIZE = 100;

typedef struct
{
    int r[MAXSIZE + 1]; // 从下标1开始存储数据，r[0]不使用
    int length;         // 顺序表的长度
} SqList;

// 初始化顺序表
void InitSqList(SqList &S, int n)
{
    S.length = n;
    for (int i = 1; i <= n; ++i)
    {
        cin >> S.r[i];
    }
}

// 快速排序
int Partition(SqList &S, int low, int high)
{
    int pivot = S.r[low]; // 选取基准元素
    while (low < high)
    {
        while (low < high && S.r[high] >= pivot)
        {
            --high;
        }
        S.r[low] = S.r[high]; // 比基准小的元素移到低端
        while (low < high && S.r[low] <= pivot)
        {
            ++low;
        }
        S.r[high] = S.r[low]; // 比基准大的元素移到高端
    }
    S.r[low] = pivot; // 基准元素放到最终位置
    return low;       // 返回基准元素位置
}

void QSort(SqList &S, int low, int high)
{
    if (low < high)
    {
        int pivotpos = Partition(S, low, high); // 划分
        QSort(S, low, pivotpos - 1);            // 递归左子表
        QSort(S, pivotpos + 1, high);           // 递归右子表
    }
}

void QuickSort(SqList &S)
{
    QSort(S, 1, S.length);
}

// 显示顺序表
void ShowList(SqList S)
{
    for (int i = 1; i <= S.length; ++i)
    {
        cout.width(5);
        cout << S.r[i];
    }
    cout << endl;
}

int main()
{
    int dt[3] = {5, 3, 1};
    int n;
    cin >> n;
    SqList S;
    InitSqList(S, n); // 初始化SqList，并输入n个整数
    QuickSort(S);     // 快速排序
    ShowList(S);      // 每个KEY值占5个字符位置

    return 0;
}
